// 本程序演示常量中的数据类型只可以是布尔，数字（整数、浮点和复数）和字符串类型
package main

import "fmt"

// B is a bool type const var
const B = true

// Pi is an numeric type const var
const Pi = 3.145926

// Str is a string type const var
const Str = "Hello world"

func main() {

	fmt.Println(B)
	fmt.Println(Pi)
	fmt.Println(Str)
}
